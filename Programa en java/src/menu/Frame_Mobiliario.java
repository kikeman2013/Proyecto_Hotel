    private void jbtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEliminarActionPerformed
        if(id_select!=0)
        {
            try {
                String sql = "call eliminar_mobiliario("+id_select+")";
                Statement ps = con.createStatement();
                ps.executeUpdate(sql);
                ps.close();
                f.actulizar_sesion();
            } catch (Exception e) {
                System.out.println( e );
            }
            Actualizartabla();

        }
        else{
            JOptionPane.showMessageDialog(this,"No has seleccionado un Mobiliario para Eliminar","No seleccion", JOptionPane.INFORMATION_MESSAGE);
        }
        select_row=0;
    }//GEN-LAST:event_jbtnEliminarActionPerformed

