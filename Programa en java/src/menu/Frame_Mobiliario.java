 public void Actualizartabla()   
    {
    try {
       
        String sql = "SELECT mob_codigo , mob_nombre ,mob_precio FROM mobiliario ORDER BY mob_codigo";
        PreparedStatement ps = con.prepareStatement( sql );
        ResultSet rs = ps.executeQuery();
        datos.removeAllElements();
        while (rs.next()){
                int    ID      = rs.getInt     ( "mob_codigo" );
                String nombre = rs.getString  ( "mob_nombre" );
                String precio = rs.getString  ( "mob_precio" );
        fila = new Vector ();
        fila.add( ID );
        fila.add( nombre );
        fila.add(precio);
        
        datos.add( fila );
        }
        modelo = new DefaultTableModel(datos, columnas);
        jtableUsuarios.setModel(modelo);
    } catch (Exception ex) {System.out.println(ex);
    }
    }
