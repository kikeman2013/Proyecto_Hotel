    private void jbtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAgregarActionPerformed
      if(!jtxfnombre.getText().trim().equals("")&&!jtxfapellido.getText().trim().equals("")){
        String nombre =jtxfnombre.getText();
       String apellido = jtxfapellido.getText();
       int id_hab = Integer.parseInt(jcbxDisponibles.getSelectedItem().toString());
       int dias = Integer.parseInt( jcbxDias.getSelectedItem().toString());
        try {
            String sql = "call registrar_estancia('"+nombre+"','"+apellido+"',"+dias+","+id_hab+")";
            Statement ps = con.createStatement();
            ps.executeUpdate(sql);
            ps.close();

        } catch (Exception e) {
            System.out.println( e );
        }
        f.actulizar_sesion();
        fe = new Frame_Estancia(con, f, fm);
        fe.setLocationRelativeTo(null);
        fe.Actualizartabla();
        fe.Actualizarboxhabitaciones();
        fe.setVisible(true);
        this.dispose();
      }
      else{
        JOptionPane.showMessageDialog(this,
                    "Se necesitan llenar todos los campos",
                    "  Campos vacios", HEIGHT);
        }
    }//GEN-LAST:event_jbtnAgregarActionPerformed

    private void jbtnCosultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCosultarActionPerformed
        String tipo_hab = jcbxTipo.getSelectedItem().toString();
       int dias = Integer.parseInt( jcbxDias.getSelectedItem().toString());
       int precio=0;
        try {
            String sql = "SELECT consultar_precio('"+tipo_hab+"',"+dias+")";
            PreparedStatement ps = con.prepareStatement( sql );
            ResultSet rs = ps.executeQuery();
            if(rs.next())
                 precio = rs.getInt(1);
            JOptionPane.showMessageDialog(this,"El precio es: $"+precio+". total","Cosultar Precio", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println( e );
        }
        f.actulizar_sesion();
    }//GEN-LAST:event_jbtnCosultarActionPerformed

