    public  void Actualizarboxdisponibles(){
        
        try {       
        
        String sql = "SELECT hab_codigo FROM habitacion AS hab\n" +
"LEFT JOIN tipo_habitacion AS thab ON (hab.tip_hab_codigo = thab.tip_hab_codigo)\n" +
"WHERE (thab.tip_hab_descripcion like '"+tipo+"') AND (hab.hab_disponible like 'disponible');";
        PreparedStatement ps = con.prepareStatement( sql );
        ResultSet rs = ps.executeQuery();
        jcbxDisponibles.removeAllItems();
        while (rs.next()){
                int codigo      = rs.getInt(1 );       
        jcbxDisponibles.addItem(Integer.toString( codigo));
        }
            
    } catch (Exception ex) {System.out.println(""+ex);}
    }
    public  void Actualizarboxhabitaciones(){
        try {       
        String sql = "SELECT tip_hab_descripcion FROM tipo_habitacion";
        PreparedStatement ps = con.prepareStatement( sql );
        ResultSet rs = ps.executeQuery();
        jcbxTipo.removeAllItems();
        while (rs.next()){
                String nombre      = rs.getString     ( 1 );       
        jcbxTipo.addItem(nombre);
        } 
    } catch (Exception ex) {System.out.println(""+ex);}
    }
    public  void Actualizarboxdias(){
         jcbxDias.removeAllItems();
         for (int i = 0; i < 40; i++) {
             jcbxDias.addItem(Integer.toString(i+1));
         }
    }
